    public UnoCard.Color choseColor(UnoCard card)
	{    
            //if the selected card is a wild card then the pickcolor frame will be visisble and the player will get the chance to select a color
            if (card.getColor()==UnoCard.Color.WILD)
            {
		setVisible(true);
		setResizable(false);
		
		 
            }
            return card.getColor();
	}

