    private void redButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redButtonActionPerformed
         Music m = new Music();
        m.buttonClick();
	JLabel message=new JLabel("The Wild Card Color is RED");
	JOptionPane.showMessageDialog(null, message);
	allow=true;
	dispose();
	gameStage.setPidName(game.getCurrentPlayer());
        gameStage.setButtonIcons();
	
	topCardButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/unocardgamesound/cardSprites/"+game.getTopCardImage())));
        game.setCardColor(UnoCard.Color.RED);
	
    }//GEN-LAST:event_redButtonActionPerformed

