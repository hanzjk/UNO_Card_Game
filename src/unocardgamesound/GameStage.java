    //set the icons to the buttons
    public void setButtonIcons() {
	String listString =game.getPlayerhand(game.getCurrentPlayer()).stream().map(Object::toString).collect(Collectors.joining(","));
	String cardNames[]=listString.split(",");
	cardIds=new ArrayList<>(Arrays.asList(cardNames));
        
        //assign icons to buttons when they are in the players hand
	for(int i=0;i<cardIds.size();i++) {
	       cardButtons.get(i).setIcon(new javax.swing.ImageIcon(getClass().getResource("/unocardgamesound/cardSprites/"+cardIds.get(i)+".png")));
        }
        
        //set the buttons to NULL if they are not in the player's hand
	for(int j=cardIds.size();j<cardButtons.size();j++)
	{
            cardButtons.get(j).setIcon(null);
	}
		
		
	}
  
    
            
