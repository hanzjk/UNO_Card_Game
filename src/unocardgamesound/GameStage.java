    //this method is called when a player selects a card button to submit a card
    public void press(String cardName,Game game,int index,ArrayList<JButton> cardButtons,GameStage gamestage,JButton topCardButton)
    {
      ArrayList<UnoCard>playerHand;
   
       UnoCard.Color declaredColor; 
       
	this.game=game;
	playerHand=game.getPlayerhand(game.getCurrentPlayer());
	
	this.topCardButton=topCardButton;
        
        //if the player has selected a wild card then the pick color frame will be viisible otherise this will return the color of the selected card
        PickColorFrame pickColor=new PickColorFrame(game,this,topCardButton,game.getCurrentPlayer());
        declaredColor =pickColor.choseColor(playerHand.get(index));
	//color of the selected card  or the chosen color will be assigned to the declaredColor
        
	if(declaredColor!=null)
	{
          try {
              //submit the selected card
              game.submitPlayerCard(game.getCurrentPlayer(), playerHand.get(index), declaredColor);
         
          } 
          catch ( InvalidColorSubmissionException | InvalidValueSubmissionException | InvalidPlayerTurnException ex) {
               Logger.getLogger(GameStage.class.getName()).log(Level.SEVERE, null, ex);
          }
					
            
            revalidate();
            if(declaredColor!=UnoCard.Color.WILD) {
                setPidName(game.getCurrentPlayer());
                setButtonIcons();
               
               topCardButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/unocardgamesound/cardSprites/"+game.getTopCardImage())));
              
            }
	}
    }
